#include "3-calc.h"
/**
 * op_add - addition
 *@a: numbr 1
 *@b: nmbr 2
 * Return: addition of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction
 *@a: nmbr 1
 * @b: nmbr 2
 * Return: substraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 *@a : nmbr 1
 *@b : nmbr 2
 * Return: multiplication a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: nmbr 1
 * @b : nmbr 2
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 *@a : nmbr 1
 *@b : nmbr 2
 * Return: modulo of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
